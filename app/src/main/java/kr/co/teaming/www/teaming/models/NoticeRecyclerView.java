package kr.co.teaming.www.teaming.models;

public class NoticeRecyclerView {

    private String noticeStudyName;
    private String noticeDate;
    private String noticeContext;

    public String getNoticeStudyName() {
        return noticeStudyName;
    }

    public void setNoticeStudyName(String noticeStudyName) {
        this.noticeStudyName = noticeStudyName;
    }

    public String getNoticeDate() {
        return noticeDate;
    }

    public void setNoticeDate(String noticeDate) {
        this.noticeDate = noticeDate;
    }

    public String getNoticeContext() {
        return noticeContext;
    }

    public void setNoticeContext(String noticeContext) {
        this.noticeContext = noticeContext;
    }
}
